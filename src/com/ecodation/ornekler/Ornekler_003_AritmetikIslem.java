package com.ecodation.ornekler;

public class Ornekler_003_AritmetikIslem {
	public static void main(String[] args) {

		// Hesap makinasi ==> toplama,çıkarma,bolme,çarpma
		int sayi1, sayi2, toplam, cikarma, carpma, bolme;
		sayi1 = 10;
		sayi2 = 13;
		toplam = sayi1 + sayi2;
		cikarma = sayi1 - sayi2;
		carpma = sayi1 * sayi2;
		bolme = sayi1 / sayi2;

		System.out.println("toplam sonucu: " + toplam);
		System.out.println("cikarma sonucu: " + cikarma);
		System.out.println("carpma sonucu: " + carpma);
		System.out.println("bolme sonucu: " + bolme);

	}
}
